                            <ul>
                                <li> - Proposant des biens et services sur la marché de l'UE
                                </li>
                                <li> - Collectant et traitant des données à caractère personnel sur les résidents de l'UE
                                </li>
                                <li> - Collectant et traitant des données personnelles sur des résidents de l'UE, même si non implantés en UE
                                </li>
                            </ul>
