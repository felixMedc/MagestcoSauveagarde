                        <summary>Comment ?</summary>
                        <p><a href="#solutions">COMMENT ? </a>
                            Afin de déterminer les problématiques existantes, nous aurons besoin de comprendre vos enjeux, d'évaluer vos outils, de connaitre les missions et le niveau de compétences numériques de chacun. Pour cela, notre expert abordera les questions clés sur votre mode de gestion. Les étapes en seront les suivantes :
                            Faire connaissance : Pour vous apporter les solutions adaptées, il est primordial de comprendre qui vous êtes. Quelle est votre activité ? Quels sont vos effectifs ? A qui s'adressent vos services ? Quelle est votre zone de marché ?
                            Evaluer vos outils de gestion : La gestion administrative est une tâche incontournable. Bien souvent fastidieuse, elle représente en moyenne 142 jours de travail par an. Nous analyserons ensemble les outils utilisés pour la facturation, la comptabilité, la relation client, la prise de rendez-vous, ainsi que la façon dont se font les liaisons entre chacune de ces applications.
                            Conformité et sécurisation : La sécurisation des données et une question épineuse en ces temps du tout umérique. Nous ferons le point sur votre conformité RGPD afin de s'assurer que votre entreprise respecte le cadre de la loi à ce sujet. De plus, nous vérifierons ensemble que la vieille documentaire est observée en termes de documents et mentions obligatoires (attestations de TVA, devis, factures, attestations de fin de chantier, CERFA à jour...)
                            Communication : Dialoguer, se faire connaître et reconnaître... Une communication fluide et efficace est primordiale. Nous détaillerons ensemble les vecteurs mis en place au sein votre structure : de quelles façon interagissez-vous en interne/externe ? Comment vous faîtes-vous connaître du public ? Utilisez-vous les réseaux sociaux ? Mesurez-vous l'efficacité de votre communication et comment le faïtes-vous ?
                            Votre équipe et le numérique : Il est important de mesurer le degré de maïtrise de chacun en terme de numérique, et ce en fonction du rôle au sein de l'entreprise, afin d'adapter le contenu des connaissances à apporter.
                            Outils de pilotage : Les indicateurs, pourtant indispensables au contrôle de la bonne marche d'une entreprise, sont souvent négligés, voire inexistants. A ce titre, nous définirons ensemble si les outils déjà en place sont suffisants et comment ils sont exploités. Ces indicateurs sont multiples : ceux liés au temps (ex : temps d'intervention), ceux liés aux coûts (ex : déplacements, salariés), à la marge, etc.
