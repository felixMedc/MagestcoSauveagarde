                                </li>
                                <li> - Identification des acteurs clés
                                </li>
                                <li> - Diagnostic du mode opératoire de gestion des données personnelles actuel
                                </li>
                                <li> - Action de mise en conformité du mode opératoire de gestion des données personnelles
                                </li>
